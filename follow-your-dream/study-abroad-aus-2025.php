		<h3 class="page-subtitle">Study Abroad とは？</h3>
		<p>
Kids UPでは毎年、夏休み期間に海外留学プログラムを実施しています。<br>
今年はイギリス留学とオーストラリア留学の2つのプログラムを行い、多くの生徒が貴重な体験をしました。<br>
このページでは、その中からオーストラリア留学についてご紹介します。<br>
<br>
今年2回目となるゴールドコースト留学には、19名のお子さまと5名の保護者様にご参加いただきました。<br>
現地では、温かく迎えてくださるホストファミリーのお宅に滞在し、語学学校で英語を学びながら、課外活動など、多彩なプログラムを楽しみました。
		</p>






