<p class="p_normal-otherpage ">

	MiniGP世界チャンピオンの栄光を手にした和久選手。しかし、彼はその栄光に甘んじることなく、すでに次なるステージ、MotoGPへと意識を向けています。スペインでの優勝は素晴らしい成果でしたが、和久選手にとっては「大きな夢の第一歩」に過ぎません。<br>
<br>
<b>2025年、さらなる飛躍へ</b><br>
2025年、和久選手はさらなるチャレンジとして、MFJ全日本カップに参戦します。目標は表彰台、できればシリーズチャンピオンの獲得。年齢制限のないこの大会では、年上で経験豊富なライダーたちとの戦いになりますが、和久選手はすでにその挑戦に燃えています。<br>
<br>	
<b>明確な5年計画</b>
</p>
<p class="p_normal-otherpage indent">
<b>• 2025年：</b>全日本カップで表彰台に立つ<br>
<b>• 2026年：</b>アジアタレントカップで優勝<br>
<b>• 2027年：</b>レッドブル・ルーキーズカップで優勝<br>
<b>• 2028年：</b>ジュニアGPで優勝<br>
<b>• 最終目標：</b>Moto3に到達すること<br>
</p>

<p class="p_normal-otherpage">
	<br>
	今年は、全日本ロードレース選手権J-GP3クラス（NSF250）にフル参戦し、さらに台湾のTSSシリーズ（CBR250RR）にも現地チームからフル参戦します。TSSは2026年のアジアロードレース選手権に向けた重要なステップ。日本との違いに対応しながら、現地ではマシンセッティングやレース当日の流れなど、すべて英語でコミュニケーションを取り、結果を出すことを目指します。<br>
<br>
<b>「自立の年」</b><br>
また、2025年は和久選手にとって「自立の年」でもあります。これまでは家族と共に行動していたレース活動も、今後はチームと共に動き、レース当日の準備や身の回りのこともすべて自分でこなす覚悟です。国内外で一段と成長する1年になるでしょう。<br>
<br>
<b>英語の重要性を痛感</b><br>
国際舞台での経験が増える中で、和久選手の「プレッシャーへの対応力」も進化しています。MiniGP優勝後、世界中のメディアからのインタビューにも冷静に対応しましたが、専門用語や伝え方に工夫が求められる場面も多く、その中で「英語の重要性」に強く気づくようになりました。今後、ジュニアGP、 Moto3、 Moto2といった海外レースが中心になるにつれて、英語力の向上は不可欠です。<br>
<br>
<b>「言語こそがキャリアの鍵」</b><br>
和久選手は「言語こそがキャリアの鍵」であることを理解し、英語力をさらに高めていく意欲にあふれています。<br>
<br>
<b>和久選手からのメッセージ</b><br>
そんな彼のアドバイスには、年齢を感じさせない力強さがあります。<br>
<br>
「あきらめないこと。目標を立てて、それを一歩ずつクリアしていくこと。」<br>
<br>
この言葉こそ、和久選手の確かな意志と努力を体現するものであり、彼がモーターバイクレースの頂点へと上り詰めていく未来を感じさせてくれます。
</p>



