<p class="p_normal-otherpage">
	2024年シーズン、知識 隼和選手はMiniGP日本チャンピオンに輝き、世界大会でも6位入賞という快挙を達成しました。しかし、知識兄妹の挑戦はこれで終わりではありません。むしろ、ここからが本当のスタートです。今回は、隼和選手とお姉さんの知識 可穏選手にインタビューを行い、これからの目標や夢について語っていただきました。<br>
	<br>
	<b>隼和：日本チャンピオンの先に見据える夢</b><br>
	日本チャンピオンのタイトルについて、隼和選手は少し照れながらも「とても嬉しかったです」と笑顔で振り返りました。しかし、彼の思いはすでに次の舞台、全日本ロードレース選手権JGP3チャレンジクラスに向いています。今年の目標はチャンピオンを獲得し、アジア・タレント・カップ（ATC）への切符を手に入れること。そして最終的にはMotoGPチャンピオンになることが夢です。「今年はその夢にとってすごく大事な年なので、全力で頑張ります」と力強く語ってくれました。<br>
	<br>
	トレーニングにも力を入れており、最近では体力作りの一環として空手にも通い始めたとのこと。電車での移動中に外国人に英語で話しかけられることも多く、その際には英語のレッスンの成果を活かして会話することもあるそうです。「少しずつスペイン語も独学で勉強してます」と、多方面での努力を重ねています。<br>
	<br>
	<b>可穏：女子世界選手権へ、情熱を燃やす</b><br>
	一方、お姉さんの可穏選手は数年前にMiniGPを卒業し、現在は筑波ロードレース選手権 J-GP3クラスに参戦中。3月のデビューレースではいきなりナショナルクラスで優勝し、今年はシリーズチャンピオンを狙っています。さらに、規定タイムを突破すれば、全日本ロードレース選手権への出場資格も得られるため、さらなる飛躍を目指しています。<br>
	<br>
	最終的な目標は、FIM女子世界選手権（Women’s GP）に出場し、世界で戦うライダーになること。「将来はどこで走りたいですか？」という質問には、「どこでも！」と笑顔で即答。今年中にも海外でのトレーニングの可能性があり、英語力の向上にも力を入れています。<br>
	<br>
	<b>英語は世界へのパスポート</b><br>
	二人とも、今後のキャリアにおいて英語の重要性を強く感じています。インタビューやレースブリーフィングだけでなく、海外のライダーたちとの交流にも英語を活用しています。隼和選手はすでに日常生活で英語を使う機会が増えており、可穏選手も今後ますます英語が必要になると感じ、しっかり準備を進めています。<br>
	<br>
	<b>夢を追う仲間たちへ</b><br>
	そんな二人が夢を追う仲間たちに伝えたいことは、<br>
	<br>
	「たくさん練習して、絶対にあきらめないこと。そして、一歩一歩着実に進むこと」。<br>
	<br>
	情熱、努力、そして明確なビジョンを持つ知識兄妹。彼らの存在が、日本のモーターバイクレースの未来を明るく照らしています。
</p>







