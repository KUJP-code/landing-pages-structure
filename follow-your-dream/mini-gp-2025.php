			<br>
			本記事では、ワク、ハヤト、そしてカノンへのZoom独占インタビューを通じて、彼らのこれまでの経験、将来の目標、そして次なるステージへの準備について深掘りします。日本が誇る若き才能たちの挑戦に、ぜひご注目ください！



