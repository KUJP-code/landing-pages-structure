<script>
async function loadActivities() {
  const wrapper = document.getElementById('activity-swiper-wrapper');
  if (!wrapper) return console.error('Wrapper not found.');

  try {
    const response = await fetch('/events/winter-school-2025/assets/includes/activities.json');
    if (!response.ok) throw new Error(`HTTP ${response.status}`);
    const activities = await response.json();

    // Dynamically collect CSS variables starting with --activity-color-
    const slider = document.querySelector('.activity-slider');
    const style = getComputedStyle(slider);

    const colors = [];
    let i = 1;
    while (style.getPropertyValue(`--activity-color-${i}`).trim() !== '') {
      colors.push(style.getPropertyValue(`--activity-color-${i}`).trim());
      i++;
    }

    const specialColor = style.getPropertyValue('--activity-color-special').trim();

    activities.forEach((activity, index) => {
      const card = document.createElement('div');
      card.classList.add('swiper-slide', 'swiper-card');

      // Assign color from CSS variables
      const color = activity.special_day ? specialColor : colors[index % colors.length];
      card.style.backgroundColor = color;
      card.style.borderColor = color;

      card.setAttribute('data-modal-target', '#eventModal');
      card.setAttribute('data-title', activity.title || 'No Title');
      card.setAttribute('data-date', activity.date || 'No Date');
      card.setAttribute('data-img', activity.image_src || '');
      card.setAttribute('data-details', activity.details || 'No details provided.');

      // Inner HTML
      card.innerHTML = `
        <div class="img-wrapper">
          <img src="${activity.image_src}" alt="${activity.title}">
          ${activity.morning ? '<h5 class="time-container">午前</h5>' : activity.afternoon ? '<h5 class="time-container">午後</h5>' : ''}
          <div class="date-container">
            <h5>${activity.date}<span class="h5-sub">(${activity.date_sub || ''})</span></h5>
          </div>
        </div>
        <div class="card-lower">
        <h4>${activity.title}</h4>
        </div>
      `;

      // Apply colors to date/time badges
      const dateContainer = card.querySelector('.date-container');
      const timeContainer = card.querySelector('.time-container');
      if (dateContainer) dateContainer.style.backgroundColor = color;
      if (timeContainer) timeContainer.style.backgroundColor = color;

      // Modal click
      card.addEventListener('click', e => {
        e.preventDefault();
        const modal = document.querySelector(card.dataset.modalTarget);
        if (!modal) return;

        modal.querySelector('#modal-img').src = card.dataset.img || '';
        modal.querySelector('#modal-date').textContent = card.dataset.date || '';
        modal.querySelector('#modal-title').textContent = card.dataset.title || '';
        modal.querySelector('#modal-details').textContent = card.dataset.details || '';
        modal.classList.add('open');
      });

      wrapper.appendChild(card);
    });

    // Swiper init
    if (typeof Swiper !== 'undefined') {
      new Swiper('.activity-slider .swiper', {
        slidesPerView: 1.25,
        spaceBetween: 16,
        centeredSlides: true,
        loop: true,
        navigation: { nextEl: '.activities-nav-next', prevEl: '.activities-nav-prev' },
        breakpoints: {
          576: { slidesPerView: 2, spaceBetween: 16 },
          768: { slidesPerView: 3, spaceBetween: 20 },
          992: { slidesPerView: 3.5, spaceBetween: 24 },
          1200: { slidesPerView: 4, spaceBetween: 24 },
          1400: { slidesPerView: 4.5, spaceBetween: 24 }
        }
      });
    }

  } catch (error) {
    console.error('Activity load error:', error);
    wrapper.innerHTML = `<p style="color:red;text-align:center;">アクティビティの読み込みに失敗しました。</p>`;
  }
}

