    <h3 class="article-header-generic">
    中学受験の特徴を知って賢く受験攻略！
    </h3>

    <p>Kids UP経営戦略室の中川です。実は私の長男が今年2月に中学受験を終えました。息子の中学受験を経験した身からすると、受験の知識がないと親も右往左往しますし、お子様も受験戦争で苦しむことになります。中学受験の知識は塾が教えてくれると思いがちです。しかし、塾側もビジネスですから、彼らに都合の良い情報だけしか提供されないケースもあります。そこで、親として中学受験で知っておくべき知識を共有させていただきます。<br>
      <br>
      皆さんは、「偏差値50」と聞くと、どんなイメージを持つでしょうか？低いと感じるか、高いと感じるかは人それぞれです。実は中学受験の偏差値50と、多くの方々が経験している高校・大学受験の偏差値50とは意味合いが大きく異なります！図の赤色に塗られている部分がそれぞれの受験者の割合を示しています。中学受験における首都圏の受験者は小学校全体の上位20％、全国においては10％程度の割合に過ぎません。つまり、中学受験人口と高校・大学受験人口は全く異なるのです。<br>
      <br>
      私立中学の多くは中高一貫校ですが、中学受験時点で「偏差値50」は、高校受験において10～15も上振れます。例えば、私立中学入学時の偏差値が45の学校の場合、高校入学時の偏差値は60になるケースはザラです。要するに、私立中学の偏差値40は、私たちの体感では偏差値50前後に相当します。「偏差値40は低すぎる！うちの子にはあり得ない！」なんて思っている保護者がいれば、その考えこそがお子様を苦しめ、受験虐待に追い込むことにもなり兼ねないので注意が必要です。（つづく）</p>

      <img src="./2024-vol-6-assets/images/article-1-chart.svg" alt="article-1-chart" class="article-1-chart">

