    <h3 class="article-header-generic" >
      スクール内での英語環境強化のお知らせ
    </h3>

    <p>Kids UPでは当初より「オールイングリッシュ」の環境でレッスンを行い、クラス内の公用語を英語とし運営してきました。
      現在、レベルチェックテストやTOEFLの結果では、インターナショナルスクールに遜色ない結果も出ております。今後、さらに本格的な英会話スクールを目指し、今まで以上にオールイングリッシュ環境を強化していきます。<br>
      <br>
      GALAXYレベル以上の生徒は、日常会話に必要な知識を一通り身に付けている状況にあります。下級生にとって憧れの存在でもありますので、まずはGALAXYレベル以上の生徒からオールイングリッシュを強化できればと思っております。<br>
      <br>
      現在GALAXYレベル以上に所属するお子様を持つ保護者様におかれましては、お子様に「スクール内で日本語を使わない」よう意識付けのご協力をお願いします。英語環境を強めていくことで、より高い結果を追求していきます。
      保護者様におかれましても、上記の内容についてお子様への指導およびサポートのご協力をお願いいたします。</p>


