  <br>
  <b>家族での練習</b><br>
  ご家族と一緒にスピーチの練習をすることで、より楽しい体験が得られます。練習が進んだら、家族内でスピーチコンテストを開催してみてはいかがでしょうか。楽しく人前で話す経験が、自信につながります。
