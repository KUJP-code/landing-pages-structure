<div class="article-1-wrapper nakagawa-article" id="article-2">
<div class="container">

  <article>
<img src="./assets/images/nakagawa-header.jpg" alt="nakagawa-header" class="img-fluid">

<div style="position: relative; display: inline-block; ">

    <p  style="padding-top: 2rem;">
      子どもの成長は臨界期の過ごし方で大きく変わります。臨界期とは、「人間の脳には学習に適切な時期があり、その時期を過ぎると学習が非常に困難になってしまう限界年齢」です。Kids UPの説明会でも時間を割いてお伝えしていますが、重要なポイントは以下の3点です。<br>
<br>
<b>① 臨界期の間に訓練を始める<br>
  ② できる限り早い年齢で始める<br>
  ③ 短期間で時間を集中して投下する<br>
  </b>
<br>
多くの保護者は1番と2番は意識できていますが、3番への意識が低い傾向にあります。言語の臨界期は2～9歳と言われています。いくら2歳に近い年齢から英語をスタートしても、週1日、1時間のレッスンでは絶対にネイティブになれません！ご両親の考え方1つですが、英語（言語）が将来に必要な武器の1つとして習得させたいなら、幼少期の比較的時間に余裕がある時に最低でも週3回以上＆1日3時間以上の頻度で英語に触れること。理想は週4回以上でしたね！実は大きな成果を出すためには、最後の『1ピース』が必要です。<br>
<br>
<b>④ 親が興味を持つこと</b><br></p>

<div class="nakagawa-top-right-image">
<img src="./assets/images/nakagawa-1.jpeg" alt="" class="img-fluid">
<p class="p-sub" style="font-weight: bold;"><span style="color: #F38000; font-size: 1rem;">▲</span> 2024年5月開催グリーンプールカップ水泳選手権大会では8歳以下の部で優秀選手に選出。</p>
</div>

<p>英語の話ばかりだと成果が分かりにくいので、例のごとく私の次男（まー君）の最近のスイミング事情をお伝えしましょう。まー君は小学校3年生でスイミングスクールの選手コースに所属し、週6日、1日2時間練習しています。神奈川県内でもトップ選手の一人です。選手コース所属のお子さんは全員同じ条件で練習していますが、当然全員が同じ結果にはなりません。成果には大きな個人差が出ますが、それは本人の「意識の差」です。<br>
<br>
私はまー君に毎日声がけをしています。「今日は何泳いだ？」「タイムは計った？」「誰に勝った？」「コーチに何を言われた？」などなど。毎日、自身の泳ぎをフィードバックさせています。決して長いフィードバックは必要ありません。親が興味を持っていることを伝え、日々意識をもって練習することが大事なのです。タイム伸びたら褒め、伸び悩んだ場合は適度に励ます。親もYouTubeや書籍で勉強して、適度にアドバイスをする。このように親が興味を持ち、二人三脚で目先の目標を設定し、共に喜び、共に悔しがってあげることが大事です。</p>
<br>

<div class="nakagawa-bottom-left-image">
<img src="./assets/images/nakagawa-2.jpeg" alt="" class="img-fluid">
<p class="p-sub" style="font-weight: bold;"><span style="color: #F38000; font-size: 1rem;">▲</span> 所属クラブのみんなと表彰式後に記念撮影。</p>
</div>
<p>
まー君は11月生まれなので、生まれが早い4～5月の子に比べると、身体的に不利になります。神奈川県トップ選手は全員4～5月生まれですが、これまでトップ選手には適いませんでした。2024年12月1日（日）のクラブ別対抗戦があり、背泳ぎ50mで神奈川県トップ選手（常に1～2位を争う選手）にはじめて勝ちました。まー君にとっては大きな成功体験であり、大きな自信でしょう。子どもの成長を最大限に伸ばしてあげたいなら、まずは時間を集中投下することが最低条件！さらに親が子どもの習い事に強い興味を持ってあげること。そうすれば、さらに伸びる可能性が広がります。「まー君の成長に手応えを感じていますか？」と問われれば、私の答えは「Yes, of course!」です。
</p>
</div>


