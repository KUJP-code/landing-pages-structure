  <h3 class="article-header-generic">新年あけましておめでとうございます</h3>

  <p>保護者の皆様には、Kids UPの活動にご理解とご協力をいただき、心より感謝申し上げます。<br>
    今年2025年、私たちが「学童保育型英会話スクールKids UP」の運営を始めて10年目という大きな節目の年を迎えます。これまで多くの子どもたちと保護者の皆様に支えられてきたことに、改めて感謝の気持ちでいっぱいです。この節目の年を新たな挑戦のスタートと位置づけ、さらなる品質向上を目指してまいります。<br>
    さて、日本の子どもたちの英語力についてですが、文部科学省が実施した英語教育実態調査によると、「聞く・話す」力の向上が顕著であると報告されています。しかし一方で、英語能力の国際比較を行う「EF English Proficiency Index」では、日本は112カ国中80位という結果でした。この現状から、日本の英語力は改善しつつあるものの、世界の伸びと比較すると非常に厳しい状態にあることが分かります。<br>
    英語を「学ぶもの」として捉えがちであり、「使える力」として発揮する場が少ないことが、この現状の一因ではないかと感じています。<br>
    私たちは、「最適な年齢に、最適な方法で、高い英語力を身につけさせることで、子どもたちの可能性を大きく拡げる」という会社理念のもと、子どもたちが日常的に英語を「使いながら学ぶ」ことを重視しています。その成果は、多くの保護者の皆様からの声、外部テストの結果、そしてスピーチコンテストでの子どもたちの堂々とした発表に表れています。<br>
    今年は、子どもたちの「未来を生きる力」を育むため、新しいプログラムにさらに取り組む予定です。海外留学プログラムや多様な体験イベントを通じて、英語を使いながら世界を広げ、自分自身の可能性を発見する機会を提供したいと考えています。これらの取り組みを通じて、子どもたちにとって単なる学びではなく、一生活かせる知識、経験、財産となることを祈りつつ企画しています。<br>
    2025年が皆様とお子様にとって、笑顔と成長に満ちた一年となりますよう、心よりお祈り申し上げます。本年もどうぞよろしくお願いいたします。
