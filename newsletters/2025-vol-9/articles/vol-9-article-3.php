<p>
  Kids UPのマスコットたちは、スクールで過ごす子どもたちの「ガイド役」です。<br>
ルールやお約束を守りやすくするために、さまざまなところに隠れています。<br>
　・入室してカードをスキャンするときには、入口でキャラクターがお出迎え。<br>
　・「Line up（列になって並ぶ）」のときには、マスコットの足跡の上に立って、順番を待ちます。<br>
<br>
こうした仕掛けのおかげで、子どもたちはキャラクターと一緒に「Learn with Fun」を体験しながら、安全に、そして楽しくスクールで過ごせます。<br>
<br>
