<div class="main-contents">
<div class="inner-text">
<p>
  Adventure（わくわく）企画室の佐藤です。<br>
この夏、3回目となるサマーキャンプを実施しました。参加したのは抽選に当たった40名。その中で見られた子どもたちの成長は、私たちの理念を改めて実感させてくれるものでした。<br>
<br>
Kids UPのキャンプが大切にしているのは、「自分のことは自分でやる」という姿勢です。テントを立て、火をおこし、食事を準備する。便利な生活に慣れた子どもたちにとっては、必ずしも楽しいことばかりではありません。ときには上手くいかず、悔し涙を流す子もいます。しかし、それを仲間と一緒に乗り越えることで、責任感や協力の大切さを実感していきます。<br>
<br>
私たちは、あえて子どもたちに大変な経験をさせたいと考えています。なぜなら、「挑戦」から「成功体験」が生まれ、それが「自信」へとつながり、やがて「未来を生き抜く力」となるからです。キャンプどころか、親元を離れての外泊が初めての子もたくさん参加してくれました。最初は不安や戸惑いがあってこそ、最後に自分で「できた！」という喜びが大きくなるのです。<br>
<br>
今回のキャンプでは、最後には別れを惜しんで涙する子が多くいました。泣いていた子たちは2日目の夜にホームシックで泣いていた子たちばかりでした。5日間の濃密なプログラムだからこそ、短期間の中で友情が芽生えたのだと思っています。<br>
<br>
Kids UPの理念は、『安全で快適な環境の中で、子どもたちに多くの「挑戦と体験」を与えることで「自信」と「未来を生き抜く力」を身に着けさせる。』ことです。今回のキャンプは、その理念をまさに形にしたものでした。<br>
<b>来年は、もっと多くの子どもたちにこの挑戦の場を届けたい。</b><br>
ご家庭でもぜひ「次はどんなことに挑戦してみたい？」とお子さまと話し合っていただければ幸いです。Kids UPはこれからも、皆さまと共に子どもたちの未来を支える学びを実現してまいります。
</p>
