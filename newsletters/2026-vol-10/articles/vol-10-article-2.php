  <h4>Q1. 当時、苦労したことや印象に残っているエピソードはありますか？</h4>

  <p>
  <br>
    <span class="speaker">Jack：</span>
    お子様たちとの思い出は数えきれないほどありますが、特に心に残っているのは、あるお子様との数年ぶりの再会です。ご入会前の説明会で一度お会いしたきりだったのですが、数年後、私がスクールに立ち寄った際、その子は私の顔を見るなり「あ！知ってる！ティーチャー・ジャックでしょ？」と、目を輝かせて駆け寄ってきてくれました。<br>
    <br>
    こうした心の繋がりを感じるたび、私たちの関わりは単なる学習の提供ではなく、お子様の人生に触れる大切なものであると強く実感します。私たちの役割は、単に英語を教えることだけではありません。数年経った後も、お子様の心の中にポジティブな記憶が残り続けること。それこそが、Kids UPで私たちが提供したい真の価値であると信じています。<br>
    <br>
    <span class="speaker">Karen：</span>
    私はKids UPの立ち上げに携わり、田園調布校で初めてお子様たちをお迎えした日の高揚感は、今も鮮明に心に残っています。数ある思い出の中でも、初めて開催した「スピーチ/発表会」は、私の原点とも言える特別な経験です。<br>
    <br>
    ステージの上で一生懸命に練習の成果を披露するお子様たちの姿と、それを全力で支えてきたティーチャーたちの情熱が一つになった瞬間、あふれる感動を抑えることができませんでした。Kids UPは単なる英語教室ではなく、お子様たちの未来をポジティブに切り拓く力を育む場所である。その時の感動は、今も私を突き動かす大きな原動力となっています。
   
  </p>

    <h4>Q2. スクールの文化や教育方針は、当時と比べてどのように変化したと感じますか？</h4>

  <p>
    <span class="speaker">Daniel：</span>
    Kids UPの教育方針は開校以来さらなる進化を続け、現在はよりアカデミックで体系的なものへと変化を遂げています。以前の「学習の歩みや努力」を大切にする評価制度に加え、現在は年2回の定期的なレベルチェックや、着実なステップアップを支援する「レベルアップ準備プログラム」を導入。さらに、外部評価（ピアソン英語ベンチマーク）を積極的に活用することで、客観的なデータに基づいたお子様一人ひとりへのきめ細かなサポート体制を確立しました。<br>
    <br>
    指導教材においても大きな飛躍を遂げています。従来のフラッシュカードなどの視覚教材をベースに、現在は顧問である萓教授の監修のもと、全レベルにおいて独自のオリジナル教科書を開発しました。明確な学習目標に基づいたこの自社教材とレベルチェックシステムを組み合わせることで、お子様たちに「本格的な英語習得への確かな道筋」を提示できる、強固な教育基盤を築き上げたと自負しております。<br>  
    <br>
    <span class="speaker">梁川：</span>
    Kids UPは、長時間英語に浸る環境を大切にしてきました。生活を共にする中で自然に英語を習得する従来の強みを活かしつつ、現在は「英会話スクール」としての教育の専門性をより一層高めています。<br>
    <br>
    特に「Keep UPコース」や「Specialistコース」といった上級プログラムの充実に伴い、中高生になっても通い続けるお子様が増えています。お子様の成長段階に合わせた、よりアカデミックで知的好奇心を刺激するカリキュラムを強化し、着実な成果を実感いただけるよう進化し続けています。<br>
  </p>  

    <h4>Q3. 今後、スクールをどのような場所にしていきたいと考えていますか？</h4>  

  <p>
      <span class="speaker">Daniel：</span>
      私たちは、英語こそがお子様の人生における無限の可能性を解き放つ「鍵」になると信じています。体系的な言語学習、実践的なCLIL（内容言語統合型学習）レッスン、そして自信を育むプレゼンテーション練習。これらを通じて、お子様が将来の土台となる確かなスキルを身につけ、自分らしく羽ばたけるまで責任を持って導いてまいります。<br>
      <br>
      これから先、Kids UPの輪がさらに広がり、より多くのお子様が私たちのカリキュラムと、情熱あふれるチームに出会えることを願ってやみません。Kids UPがお子様の未来を切り拓く「最高のスタート地点」であり続けるために、私たちはこれからもたゆまぬ努力を続けてまいります。<br>
      <br>
      <span class="speaker">諸橋：</span>
      私自身、これまで現場でお子様や保護者様、そして信頼できるチームの仲間に支えられ、共に歩ませていただく中で多くの学びと成長の機会をいただきました。教育の現場は責任の重さを感じる場面も多々ありますが、それ以上に「自分たちの関わりがお子様の確かな成長に繋がった」「保護者様と共にお子様の喜びを分かち合えた」という実感が、何よりの原動力となっています。<br>
      <br>
      Kids UPのスタッフ一人ひとりにも、日々の関わりを通して「誰かの力になれている」という喜びを分かち合ってほしいと願っています。スタッフが情熱とやりがいを持って向き合うことが、結果としてお子様へのより良い教育環境を創り出すと信じています。これからも、スタッフ一人ひとりが輝き、そのエネルギーをお子様の未来へと還元していけるような、活気あふれるスクールを築いてまいります。  
