<h4>Q2. 長期間通学を続けてこられた理由は何ですか？</h4>

<p><span class="bold">母：</span>長期間通っている理由は、本人が英語が好きなこと、体験学習ができること、自宅から近く通いやすかったことです。<br>
<br>
<span class="bold">R：</span>スピーチコンテストなどで競う事はやる気が出るし楽しいです。普段のアクティビティも楽しいし、このままずっと続けたいです。
</p>

<h4>Q3. 学校の友達と比べて、自分の英語力をどのように感じますか？</h4>

<p><span class="bold">R：</span>学校では英語が全く話せない子が多いですが、自分は帰国子女の子と同じくらい英語を話すことができます。学校の英語も難しくないのでとても自信になります。</p>

<h4>Q4. 英語の成果をどんな時に感じますか？</h4>

<p><span class="bold">R：</span>2年くらい前に外国人観光客に道を聞かれて英語で案内することができました。また、外国人同士で英語で話していても会話の内容を理解できます。<br>
  <br>
<span class="bold">母：</span>Kids UP以外では、学校の授業で発音を褒められること、英検合格が嬉しいようです。
</p>

<h4>Q5. Kids UPでは中学生・高校生の生徒も増えていますが、今後も継続したいというお考えはありますか？</h4>

<p><span class="bold">母：</span>今までに退会を検討したことはありませんが、中学入学後はどうしようかなと思っているところです。<br>
  <br>
  <span class="bold">R：</span>学校や今後の進路を考えても英語力が役に立つのでやっていて良かったです。Kids UPに通っていれば英語力が落ちないので可能な限りこのまま続けたいです。
</p>

<h4>Q6. 小学校とKids UPの両立を頑張ってきた中で、ご両親に伝えたいことはありますか？</h4>
<p><span class="bold">R：</span>自分が「習いたい」と言って始めた訳ではありませんでしたが、英語が話せるようになって嬉しいし、習っていて良かったと思うので、お父さんとお母さんには「Thank you.」という思いです。将来の具体的な夢は決まっていませんが、日常的に英語を使っていきたいです。<br>
<br>
<span class="bold">母：</span>英語学習をがんばっていて偉いなと思います。これからも楽しみながら続けられると良いですね。
</p>

