<h4>Q1. Kids UPに通い始める前、英語経験はどの程度ありましたか？</h4>

<p><span class="bold">母：</span>きょうだい2名とも、英語の経験はまったくありません。本当にゼロからのスタートでした。<br>
  <br>
  <span class="bold">コウキ：</span>正直あまり覚えていないのですが(笑)、「英語は難しい」と感じたことは記憶に残っています。

</p>

<h4>Q2. Kids UPでの思い出や、印象に残っていることはありますか？</h4>
<p><span class="bold">シオリ：</span>色々なレッスンやイベントに英語を使いながらみんなで参加できたのが印象に残っています。クリスマスやハロウィンなどのイベントは特に楽しかったです。<br>
<br>
<span class="bold">母：</span>通い始めたころは毎日泣いていてとても心配でしたが、ある時期から泣かなくなり、少しずつ慣れていって、気づけば楽しく通えるようになりました。泣いていたのを懐かしく思います。
</p>

<div class="img-box">
<img src="./assets/images/section-4/twins-christmas.jpg" alt="" class="img-fluid">
