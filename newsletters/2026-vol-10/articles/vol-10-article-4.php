<h2>【 感謝を胸に、次の10年へ 】</h2>
<p>今回ご紹介したインタビューの言葉は、特別な誰かの物語ではなく、Kids UPで学ぶ日常の延長線上にある、等身大の声です。長く通う中で感じた喜びや悩み、挑戦と成功体験の一つひとつが、英語力はもちろん、自ら考え行動する力や自信へとつながってきました。<br>
これらの声は、これまでKids UPに通ってくださったすべての生徒・保護者の皆さまの歩みそのものでもあります。10周年は、その積み重ねに改めて感謝を伝える節目であり、同時に子どもたちがこれからの未来を生き抜く力を育んでいく、学びの通過点です。<br>
Kids UPは、これからも一人ひとりの声に耳を傾けながら、英語を通して子どもたちの可能性を広げ、未来に寄り添い続けてまいります。 </p>

