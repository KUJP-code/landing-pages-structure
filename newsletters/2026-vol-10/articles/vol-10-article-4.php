<h4>Q3. 長期間通学を続けてこられた理由は何ですか？</h4>
<p><span class="bold">母：</span>英語は早く始めたいという思いがありましたが、途中で途切れてしまうと意味がないとも感じていた為、無理なく続けられる環境だったことが大きいです。<br>
先生方が子どもの様子に合わせて、常に工夫して関わってくださり、担当の先生が変わっても安心して続けられているのは、Kids UP全体の環境が整っているからだと感じています。
</p>

<h4>Q4. 英語の成果をどんな時に感じますか？</h4>
<p><span class="bold">母：</span>近所にイギリスとのハーフのお子さんがいて、その子と自然に英語で会話ができていた時です。Kids UP以外の場面で英語が使えているのを見て、成長を実感しました。<br>
  <br>
   <span class="bold">コウキ：</span>学校で英語のスピーチをする時は文章をすぐに作ることができて、テストでも満点を取ることができて、友だちからは「すごいね！」と言われます。Kids UPの先生とはもう英語だけで話せるので、イギリスやアメリカなど、海外で英語を話してみたいです。
</p>

<h4>Q5. Kids UPでは中学生・高校生の生徒も増えていますが、今後も継続したいというお考えはありますか？</h4>
<p><span class="bold">母：</span>中学生になると忙しくなりますが、ここまで続けてきたからこそ、できる限り続けさせてあげたいという気持ちはあります。<br>
<br>
<span class="bold">シオリ：</span>続けたい気持ちはあります。でも、学校との両立は忙しそうだなとも思います。
</p>

<h4>Q6. 通学歴がまだ短いご家庭へ、長期通学を実現するためのアドバイスをお願いします。</h4>
<p><span class="bold">母：</span>まずはやってみないと分からないと思いますが、子どもが自分だけで頑張るだけでなく、親も通うためのサポート（イベントに参加させてあげる等）をしてあげられるとよいと思います。</p>

<h4>Q7. 小学校とKids UPの両立を頑張ってきた中で、ご両親に伝えたいことはありますか？</h4>
<p><span class="bold">シオリ：</span>英語が話せるようになって嬉しいです。通わせてくれて、ありがとうございます。いつも感謝しています！将来は日本で困っている外国人の人がいたら、英語で教えてあげて、世界中の人の役に立てたらいいなと思います。<br>
<br>
<span class="bold">コウキ：</span>英語を長い間続けてきて、「こんなに話せるようになった」ということを伝えたいです。<br>
<br>
<span class="bold">母：</span>コツコツと、本当によく頑張って続けてきたね。楽しい時も、ちょっと大変な時もあったと思います。これからは、今まで積み重ねてきた英語を、自分らしく使って、世界を広げていってください。
</p>
