        <h2>
【“継続”が育てた英語力〜通い続けてくれた皆さまの声〜】
  </h2>
  </div>

  <p>10周年を記念し、長期間にわたりKids UPに通学いただいている生徒・保護者の皆さまにインタビューを行いました。英語力の成長はもちろん、レッスンやイベントを通して積み重ねてきた思い出、そしてこれからの目標や未来への想いから、Kids UPの10年の歩みを感じていただけます。<br>
      これからお通いになる方、通学歴の浅い方にとっても、Kids UPでの学びを続けていくうえでのヒントや励みとなる内容です。
</p>

