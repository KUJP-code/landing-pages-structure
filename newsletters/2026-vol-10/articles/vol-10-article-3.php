<script>
document.addEventListener("DOMContentLoaded", () => {
  document.querySelectorAll(".section-3 .parallaxSwiper").forEach((swiperEl) => {
    
    // Safety check: Prevent double initialization
    if (swiperEl.swiper) return;

    function equalizeSlideHeights(swiper) {
      let maxHeight = 0;
      swiper.slides.forEach(slide => {
        slide.style.height = "auto";
      });
      swiper.slides.forEach(slide => {
        maxHeight = Math.max(maxHeight, slide.offsetHeight);
      });
      swiper.slides.forEach(slide => {
        slide.style.height = `${maxHeight}px`;
      });
      swiper.update();
    }

    const swiper = new Swiper(swiperEl, {
      speed: 600,
      slidesPerView: 1,
      parallax: true,
      // If this slider needs arrows too, you MUST add the navigation block here:
      navigation: {
        nextEl: swiperEl.querySelector(".swiper-button-next"),
        prevEl: swiperEl.querySelector(".swiper-button-prev"),
      },
      pagination: {
        el: swiperEl.querySelector(".swiper-pagination"),
        clickable: true,
      },
      on: {
        init() { equalizeSlideHeights(this); },
        resize() { equalizeSlideHeights(this); },
        imagesReady() { equalizeSlideHeights(this); },
        slideChange() {
          if(this.pagination) {
            this.pagination.render();
            this.pagination.update();
          }
        }
      },
    });
  });
});
</script>