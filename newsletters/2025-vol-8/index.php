  document.addEventListener('DOMContentLoaded', function() {
  // --- Configuration ---
  const fixedHeader = document.getElementById('template-nav'); // Replace with your actual fixed header ID
  const additionalOffset = 20; // Adjust as needed (for header height)
  const retryDelay = 100; // Delay before retrying scroll (if needed)
  const maxAttempts = 3; // Maximum number of scroll attempts

  // --- Helper Functions ---
  function getFixedHeaderHeight() {
    return fixedHeader ? fixedHeader.offsetHeight : 0;
  }

  function getElementOffsetTop(element) {
    let top = 0;
    do {
      top += element.offsetTop || 0;
      element = element.offsetParent;
    } while (element);
    return top;
  }

  // --- Smooth Scroll Function with Retry Logic ---
  function smoothScrollTo(targetId, attempt = 1) {
    const targetElement = document.getElementById(targetId);

    if (targetElement) {
      try {
        const targetTop = getElementOffsetTop(targetElement) - getFixedHeaderHeight() - additionalOffset;
        window.scrollTo({
          top: targetTop,
          behavior: 'smooth'
        });
      } catch (error) {
        console.warn(`Scroll failed (attempt ${attempt}):`, error);
        if (attempt < maxAttempts) {
          setTimeout(() => {
            smoothScrollTo(targetId, attempt + 1); // Retry
          }, retryDelay);
        } else {
          console.error('Max scroll attempts exceeded. Giving up.');
        }
      }
    } else {
      console.warn('Target element not found:', targetId);
    }
  }

  // --- Handle Internal Link Clicks ---
  document.querySelectorAll('a[href^="#"]').forEach(link => {
    link.addEventListener('click', function(event) {
      event.preventDefault();
      const targetId = this.getAttribute('href').substring(1);
      smoothScrollTo(targetId);
    });
  });
});
