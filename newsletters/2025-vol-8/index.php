  <h2>Kids UPで頑張る中高生（中学受験生）</h2>
  <p>Kids UPでは、「大人になってからも使える英語力」の習得を目指しています。上級者コースとして開校している「Keep UPコース」や「Specialistコース」では、中学・高校では学ぶ機会が少ない高度なトピックを扱い、プレゼンテーションやディベート、海外大学で行われるようなリサーチワークを実践します。また、子どもたちは毎年恒例のスピーチコンテストを通じて、人前で表現する力を磨いています。<br>
    <br>
    現在、Kids UPには約40名の中高生が在籍しています。2025年4月には、現在小学6年生の子どもたちが加わり、およそ80名規模へと拡大する予定です。義務教育における英語学習は小学3年生から始まり、中学受験でも英語を採用する学校が年々増えています。首都圏の私立中学校約350校のうち、約150校が英語を受験科目として導入しています。また、多くの学校が英検3級（中学卒業レベル）以上の英語力を求めています。<br>
    <br>
    私立中学入試に英語を取り入れる学校が増える一方で、英語が得意な子とそうでない子の間で学力差が問題となっています。また、英語受験を行っていない私立中学でも同様の問題が起きています。私立中学の多くは、高い英語力を持つ生徒を優遇する傾向があるため、英語に苦手意識を持つ子どもは授業についていくのが難しくなるリスクがあります。こうした背景もあり、Kids UPに通い続ける中高生の多くは「学校の英語授業では物足りない」、「英語力を維持・向上させたい」と考え、上級者コースで学び続けています。<br>
    <br>
    実際、Kids UPで学ぶ中高生の多くは、私立・公立を問わず在籍する学校でトップクラスの成績を収めています。中には、英検準1級の取得、大学入学共通テストの英語満点、全国模試1位、通知表で英語は常に5（最大5）といった成果を出しながらも、さらなる成長を求めて学び続ける姿勢には目を見張るものがあります。「継続は力なり」を体現する生徒たちの姿は、まさに誇るべきものです。<br>
    <br>
    この記事を読んでいる保護者のお子様も、幼少期から英語を学びはじめたことで、英語を強みとして活かせる立場にいます。中学受験を予定しているご家庭も多いと思いますが、受験勉強を優先するために英語学習を断念するのは、非常に勿体ない選択です。受験合格はゴールではないのです！英語は中学進学後に本格的に学び始める教科ですが、事前に十分な力をつけておけば、大きなアドバンテージを持って中高生活を謳歌できることでしょう。英語を受験科目に採用する学校が増えている今こそ、英語を武器にした中学受験を検討してみてはいかがでしょうか？
